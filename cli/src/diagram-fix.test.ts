describe('charDisplayWidth', () => {
  test('ASCII chars are width 1', () => {
    expect(charDisplayWidth('a')).toBe(1)
    expect(charDisplayWidth('Z')).toBe(1)
    expect(charDisplayWidth(' ')).toBe(1)
    expect(charDisplayWidth('в”Җ')).toBe(1)
    expect(charDisplayWidth('в”Ӯ')).toBe(1)
  })

  test('CJK chars are width 2', () => {
    expect(charDisplayWidth('дёӯ')).toBe(2)
    expect(charDisplayWidth('ж—Ҙ')).toBe(2)
    expect(charDisplayWidth('жң¬')).toBe(2)
  })

  test('box-drawing chars are width 1', () => {
    expect(charDisplayWidth('в”Ң')).toBe(1)
    expect(charDisplayWidth('в”җ')).toBe(1)
    expect(charDisplayWidth('в””')).toBe(1)
    expect(charDisplayWidth('в”ҳ')).toBe(1)
    expect(charDisplayWidth('в”¬')).toBe(1)
    expect(charDisplayWidth('в”ҙ')).toBe(1)
  })
})

describe('stringDisplayWidth', () => {
  test('ASCII string', () => {
    expect(stringDisplayWidth('hello')).toBe(5)
  })

  test('mixed ASCII and CJK', () => {
    expect(stringDisplayWidth('hiдёӯж–Ү')).toBe(6) // 2 + 2 + 2
  })

  test('box border', () => {
    expect(stringDisplayWidth('в”Ңв”Җв”Җв”Җв”Җв”Җв”Җв”җ')).toBe(8)
  })
})

