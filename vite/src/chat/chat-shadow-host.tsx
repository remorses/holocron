'use client'

/**
 * ChatShadowHost — renders chat components inside a Shadow DOM for style
 * isolation when the widget is embedded in third-party pages.
 *
 * CSS custom properties penetrate the shadow boundary for theming.
 * All portals (drawer backdrop, tooltips) target the shadow mount container
 * instead of document.body.
 *
 * Follows the spiceflow RemoteIsland pattern:
 * - attachShadow({ mode: 'open' })
 * - inject bundled CSS via <style>
 * - createRoot into [data-mount] inside shadow
 *
 * No SSR/DSD needed — the widget is client-only.
 */

import React, { useLayoutEffect, useRef } from 'react'
import { createRoot, type Root } from 'react-dom/client'

// Combined CSS (Tailwind utilities + chat.css) as a string constant.
// Generated by scripts/bundle-chat-css.ts — works after tsc without Vite.
import { chatCssBundle } from './chat-css-bundle.ts'

export function ChatShadowHost({
  children,
  className,
  style,
  dark,
  onMountPoint,
}: {
  children: React.ReactNode
  className?: string
  style?: React.CSSProperties
  /** When true, applies the .dark class to the shadow host for dark mode. */
  dark?: boolean
  /** Called with the mount point element once the shadow DOM is ready.
   *  Use this to set the portal target in chatWidgetStore. */
  onMountPoint?: (mount: HTMLElement) => void
}) {
  const hostRef = useRef<HTMLDivElement>(null)
  const rootRef = useRef<Root | null>(null)
  const childrenRef = useRef(children)
  childrenRef.current = children

  useLayoutEffect(() => {
    const host = hostRef.current
    if (!host) return

    const shadow = host.shadowRoot ?? host.attachShadow({ mode: 'open' })

    // Inject CSS once
    if (!shadow.querySelector('style[data-holocron-chat]')) {
      const styleEl = document.createElement('style')
      styleEl.setAttribute('data-holocron-chat', '')
      styleEl.textContent = chatCssBundle
      shadow.appendChild(styleEl)
    }

    // Find or create mount point
    let mountPoint = shadow.querySelector<HTMLDivElement>('[data-mount]')
    if (!mountPoint) {
      mountPoint = document.createElement('div')
      mountPoint.setAttribute('data-mount', '')
      shadow.appendChild(mountPoint)
    }

    // Notify parent of the mount point (for portal target setup)
    onMountPoint?.(mountPoint)

    // Create React root and render
    const r = createRoot(mountPoint)
    r.render(childrenRef.current)
    rootRef.current = r

    return () => {
      const root = rootRef.current
      rootRef.current = null
      if (root) {
        queueMicrotask(() => root.unmount())
      }
    }
  }, [])

  // Re-render when children change
  useLayoutEffect(() => {
    rootRef.current?.render(children)
  }, [children])

  return (
    <div
      ref={hostRef}
      className={`${className || ''}${dark ? ' dark' : ''}`}
      style={style}
      data-holocron-chat-host=''
    />
  )
}
